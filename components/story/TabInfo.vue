              to accept him, root for him, and accept his brutal ways. His lack of empathy in this competitive wuxia workd becomes his strength and makes him "cool." And as if to contrast this, you also slowly begin to realise
              how deeply flawed other wuxia novels are - they say that it's competitive, but none of their characters in their story takes the same measures. Think about it, if your life is constantly on the ropes, would you
              have time to go left and right to conquer beauties or spare you rivals? No! You'd use every means at your disposal to gain strength, plot against your rivals/enemies, and further you agenda.<br />
              <br />
              This brings me to the second point; the side characters. They are pretty well sculpted, with their own agenda, ideals, and background. As of now, not much is known, but the MC's flashback unveils a larger story
              and gives more depth to the side characters. If you think the MC's a dick? Wait till you meet enemy/***** nr. 1 - it's damn near chilling what she does to gain strength. <br />
              There's no right or wrong in this world, only who's weaker or stronger. Might makes right - and in this novel, it does so in the most brutal, no-nonsense, unfiltered kind of way. Dark pleasures, anyone?<br />
              As of chapter 18, we've been introduced to some superficial side characters, and a few prominent ones - we're still learning more and more of the plot and MC, but not much has been revealed.<br />
              <br />
              This brings me to the story progression, I can see that the author is taking the long route; slowly building the characters, their relations, and unveiling a bit of the world a chapter at a time. We don't know
              why the character wants to become strong or why he's cultivating, though a little has been hinted at his meagre family background that could potentially have plot relevance. As of chapter 18, MC is just a
              ruthless, overbearing, and prideful mc, but the plot is slowly starting. <br />
              I find this good, as you need time to adjust to the new paradigm mentality and the circumstances of the MC, and I'm eagerly reading to uncover more about the MC, and thus stumble upon the overarching plot in due
              time.<br />
              <br />
              Lastly, this is a pretty new story, so I'm cautious about predicting update stability. (cont. In comments).
